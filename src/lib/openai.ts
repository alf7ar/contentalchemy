export type Platform = keyof typeof SYSTEM_PROMPTS

export interface GenerateOptions {
  platform: Platform
  topic: string
  businessName?: string
  businessType?: string
  tone?: "professional" | "casual" | "luxury" | "funny"
  targetAudience?: string
}

export interface GenerateResult {
  title: string
  content: string
  hashtags: string[]
  platform: Platform
}

export async function generateContent(options: GenerateOptions): Promise<GenerateResult> {
  const systemPrompt = SYSTEM_PROMPTS[options.platform]

  const toneMap: Record<string, string> = {
    professional: "احترافية وجادة - خلي الكلام محترم ومباشر",
    casual: "عفوية وقريبة من الناس - زي ما بتكلم صاحبك",
    luxury: "فاخرة وأنيقة - استخدم كلمات زي حصرية، نخبوية، تميز",
    funny: "فكاهية وخفيفة - ضحك وهزار، المصري بيحب الكوميديا",
    edgy: "جريئة وجسورة - استخدم كلمات صادمة وقوية، خلي الكلام واخز ومباشر",
    inspirational: "ملهمة ومحفزة - كلام من القلب، قصص نجاح، تحفيز على التغيير",
    educational: "تعليمية وتوعوية - شرح مبسط، خطوات عملية، معلومات قيمة",
  }

  const userPrompt = `معلومات النشاط التجاري:
- اسم النشاط: ${options.businessName || "[غير محدد]"}
- نوع النشاط: ${options.businessType || "[غير محدد]"}
- الموضوع: ${options.topic}
- النغمة المطلوبة: ${toneMap[options.tone || "professional"] || options.tone || "professional"}
- الجمهور المستهدف: ${options.targetAudience || "الجمهور المصري العام"}

⚠️ مهم جداً: المحتوى لازم يكون باللهجة المصرية 100%. مش فصحى. المصريين بتتكلم مصري على السوشيال ميديا.
استخدم كلمات زي: "بص"، "طيب"، "يلا"، "بجد"، "أوعى"، "الحقيقة"، "على فكرة"، يا عم/يا جماعة (حسب السياق).
لو النغمة فكاهية، استخدم النكت المصرية والمواقف اليومية.
لو النغمة احترافية، استخدم مزيج من الفصحى المبسطة والمصري المحترم.
لو النغمة فاخرة، استخدم كلمات زي "حصرية"، "تميز"، "نخبة" مع لمسات مصرية راقية.

يجب أن يكون المحتوى أصلياً 100% وجذاب ومناسب للسوق المصري.`

  const completion = await openai.chat.completions.create({
    model: "gpt-4o-mini",
    messages: [
      { role: "system", content: systemPrompt },
      { role: "user", content: userPrompt },
    ],
    temperature: 0.7,
    max_tokens: 1500,
  })

  const text = completion.choices[0]?.message?.content || ""

  const hashtagRegex = /#[\u0621-\u064A\w\u0660-\u0669]+/g
  const hashtags = text.match(hashtagRegex) || []
  const contentWithoutHashtags = text.replace(hashtagRegex, "").trim()

  const lines = contentWithoutHashtags.split("\n").filter(l => l.trim())
  const title = lines[0]?.replace(/^[*#]+\s*/, "").trim() || options.topic
  const content = lines.slice(1).join("\n").trim()

  return {
    title,
    content,
    hashtags: hashtags.slice(0, 10),
    platform: options.platform,
  }
}

export async function generateMultiPlatform(options: Omit<GenerateOptions, "platform">) {
  const platforms: Platform[] = ["instagram", "facebook", "tiktok", "linkedin", "ads"]
  const results = await Promise.all(
    platforms.map(platform => generateContent({ ...options, platform }))
  )
  return results
}
